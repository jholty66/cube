class Graph {
    adjList;
    constructor() { this.adjList = new Map(); }

    addNode = (v: number) => this.adjList.set(v, new Set());

    addEdge = (v1: number, v2: number) => {
        this.adjList.get(v1).add(v2);
        this.adjList.get(v2).add(v1);
    }
}

let adjList = new Graph();

for (let i = 0; i < adjMat.length; i++) { adjList.addNode(i); }

for (let y = 0; y < adjMat.length; y++) {
    for (let x = 0; x < adjMat[y].length; x++) {
        if (adjMat[y][x] == 1) { adjList.addEdge(y,x) }
    }
}

